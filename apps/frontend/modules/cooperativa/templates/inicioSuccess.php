<h1>Misión</h1>
<div class="table_show">
  <p>La  misión de la cooperativa Manuel Benjamín Carrión es servir con excelencia a la colectividad Lojana, encaminados  siempre al crecimiento de la organización y el mejoramiento Socio-Económico de sus asociados, siempre direccionados  con lo que faculta la ley y el Reglamento General de Cooperativas, el Estatuto y Reglamento interno y más leyes que nos rigen.</p></div>
<h1>Visión</h1>
<div class="table_show">
    <p>La visión de la cooperativa de taxis Manuel Benjamín Carrión es ser la mejor entidad de taxis en la ciudad de Loja,  mejorando la calidad de servicio a la clientela  y ciudadanía, así como también la calidad de vida de los asociados, utilizando recursos que nos permitan crecer como personas  y como organización.</p>
</div>
